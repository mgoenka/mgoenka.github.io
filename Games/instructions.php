<p align="left">
* The 'probability' associated with each gate indicates the chance that the guard will choose to protect certain gate. For example, if the probability for gate 1 is 0.3, it means that with probability of 0.3, gate 1 will be protected by a guard.  There are two guards for rounds with five doors and three guards for rounds with eight doors, each gate will be protected by at most one guard. Hence, there will be 2 and 3 doors that are protected by the guards for rounds with 5 doors and rounds with 8 doors respectively.<br/>
<br/>
* 'Your Rewards' indicates the score you will be winning if you choose to one gate and it is not protected by any guard. For example, if your reward for gate 1 is 6, then you will win 6 points if you choose to attack gate 1 and there is no guard protect gate 1. <br/>
<br/>
* 'Your Penalties' indicates the score you will be losing if you choose to one gate and it is protected by the guard. For example, if your penalty for gate 1 is -4, then you will lose 6 points if you choose to attack gate 1 and it is protected by the guard. <br/>
<br/>
* Note that, the gates that will be protected by the guards are selected randomly according to the probability.
